
public class TernaryOperator {

	public static void main(String[] args) {
		int newValue = 50;
		
		boolean isCar = true;
		
		boolean wasCar = isCar ? true : false; //if isCar is true wasCar = true. Else its false
		
		// ? ternary operator shortcut to if-then-else statement!!! NEATO
		
		if(wasCar) {
			
			System.out.println("Was car is true");
		}
		
		if(newValue == 50) {
			System.out.println("/n");
			System.out.println("This equals 50");
			
		}

	}

}
